package Groundwater1DGUI;

public class Node {
    private int n;
    private double x;
    private double w;
    private double h;
    private double q;

    public void setN(int n) {
        this.n = n;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setW(double w) {
        this.w = w;
    }

    public void setH(double h) {
        this.h = h;
    }

    public void setQ(double q) {
        this.q = q;
    }

    public int getN() {
        return n;
    }

    public double getX() {
        return x;
    }

    public double getW() {
        return w;
    }

    public double getH() {
        return h;
    }

    public double getQ() {
        return q;
    }
}
